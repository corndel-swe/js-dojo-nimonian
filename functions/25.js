/**
 * Calculates the compound interest.
 * @param {number} principal - The initial amount of money.
 * @param {number} rate - The annual interest rate (as a decimal).
 * @param {number} time - The time the money is invested for, in years.
 * @returns {number} The amount of money accumulated after n years, including interest.
 *
 * Example: compoundInterest(1000, 0.05, 10) should return 1628.89 (approximately).
 */
export function compoundInterest(principal, rate, time) {
  // TODO: return the accumulated amount using the formula A = P(1 + r)^t
  return principal * (1 + rate) ** time
}
